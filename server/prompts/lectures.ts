/**
 * Lecture (讲义) prompt templates.
 *
 * Supports two teaching styles:
 * 1. "khanmigo" — Khan Academy Khanmigo style (warm tutor beside you)
 * 2. "chatgpt-learn" — ChatGPT Learn mode style (interactive learning partner)
 */

export type LectureStyle = 'khanmigo' | 'chatgpt-learn' | 'feynman' | 'socratic' | 'first-principles' | 'harvard-tutor';

export function buildLectureOutlinePrompt(course: {
  title: string;
  description: string;
  content: string;
  requirements: string;
}, syllabus: { week: number; topic: string }[]): string {
  return `你是一位资深的课程讲师和教材编写专家。请根据以下课程信息生成讲义的章节结构。

课程标题：${course.title}
课程描述：${course.description}
学习内容：${course.content}
学习要求：${course.requirements}

教学大纲：
${syllabus.map(s => `第${s.week}周：${s.topic}`).join('\n')}

请生成讲义的章节结构，要求：
1. 将大纲中的每周主题扩展为一个"章"（chapter）
2. 每章分为 3-5 个"节"（section），编号为 1.1, 1.2, 1.3...
3. 每节只讲一个核心知识点，遵循认知负荷理论（一次只学一个概念）
4. 章节之间有递进关系：先基础后进阶，先概念后应用
5. 每节的标题要具体明确（不要用"概述"这种笼统的标题）

严格按以下 JSON 数组格式输出，不要输出任何其他内容（不要 markdown 代码块标记）：
[
  {"chapter": 1, "chapter_title": "章标题", "section_num": "1.1", "section_title": "节标题", "sort_order": 1},
  {"chapter": 1, "chapter_title": "章标题", "section_num": "1.2", "section_title": "节标题", "sort_order": 2}
]`;
}

export function buildLectureSectionPrompt(params: {
  course: { title: string; description: string; content: string };
  chapterTitle: string;
  sectionNum: string;
  sectionTitle: string;
  previousSections: { section_num: string; title: string; summary: string }[];
  syllabusTopics: string;
  style?: LectureStyle;
}): string {
  const style: LectureStyle = params.style || 'khanmigo';
  const prevContext = params.previousSections.length > 0
    ? `\n\n本章已讲解的前序小节（帮助学生建立连贯性）：\n${params.previousSections.map(s => `- ${s.section_num} ${s.title}：${s.summary}`).join('\n')}`
    : '';

  let prompt: string;
  switch (style) {
    case 'chatgpt-learn': prompt = buildChatGPTLearnPrompt(params, prevContext); break;
    case 'feynman': prompt = buildFeynmanPrompt(params, prevContext); break;
    case 'socratic': prompt = buildSocraticPrompt(params, prevContext); break;
    case 'first-principles': prompt = buildFirstPrinciplesPrompt(params, prevContext); break;
    case 'harvard-tutor': prompt = buildHarvardTutorPrompt(params, prevContext); break;
    default: prompt = buildKhanmigoPrompt(params, prevContext); break;
  }

  return prompt;
}

